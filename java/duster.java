public class duster {
    private int length;
    private int breath;
    private String color;
    public duster(int length,int breath,String color){
        this.length=length;
        this.breath=breath;
        this.color=color;

    }
    public int getlength() {
        return this.length;
        
    }
    public int getbreadth(){
        return this.breath;
    }
    public String getcolor(){
        return this.color;
    }
   
    
}
